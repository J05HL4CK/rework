# Data types in Ruby, some examples;

# The variable "textual" is assigned the class string 
textual = "I am a string of characters representing textual data"
# the variable "integer" is assigned the class integer
integer = 1
# "float" is a floating point number
float = 5.0
# "array" is an array containing numbers of different types
array = [integer, float]
# "hash" is a hash conting string: as the symbol and textual as the value
hash = {string: textual}
# Boolean true/false
array.include?(integer)
